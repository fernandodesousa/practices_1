package Prática_4_Módulo_3;

public class Charlie {
    String product_name;
    String processor;
    double screen;
    int memory;
    int storage;
    int storage_sort;
    String graphics;
    double price;


    Charlie (String product_name, String processor, double screen, int memory, int storage, int storage_sort,
             String graphics, double price) {
        this.product_name = product_name;
        this.processor = processor;
        this.screen = screen;
        this.memory = memory;
        this.storage = storage;
        this.storage_sort = storage_sort;
        this.graphics = graphics;
        this.price = price;
    }
}
